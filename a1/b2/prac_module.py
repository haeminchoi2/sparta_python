def plus(num1, num2):
    print(f"{num1} + {num2}")
    return num1 + num2

def minus(num1, num2):
    print(f"{num1} - {num2}")
    return num1 - num2

def times(num1, num2):
    print(f"{num1} * {num2}")
    return num1 * num2

def div(num1, num2):
    print(f"{num1} / {num2}")
    return num1 / num2